fn render_with_start_functions(out: &mut String, svc: &ServiceModel) {
    for wf in &svc.workflows {
        for sref in &wf.attached_signals {
            if !sref.start {
                continue;
            }
            let Some(sig) = svc.signals.iter().find(|s| s.rpc_method == sref.rpc_method) else {
                continue;
            };
            render_signal_with_start_fn(out, svc, wf, sig);
        }
        for uref in &wf.attached_updates {
            if !uref.start {
                continue;
            }
            let Some(u) = svc
                .updates
                .iter()
                .find(|uu| uu.rpc_method == uref.rpc_method)
            else {
                continue;
            };
            render_update_with_start_fn(out, svc, wf, u);
        }
    }
}

fn render_signal_with_start_fn(
    out: &mut String,
    svc: &ServiceModel,
    wf: &WorkflowModel,
    sig: &SignalModel,
) {
    // Free function lives alongside the client struct so callers can pass a
    // raw `TemporalClient` without constructing the client wrapper.
    let fn_name = format!("{}_with_start", sig.rpc_method.to_snake_case());
    let handle_struct = format!("{}Handle", wf.rpc_method);
    let opts_struct = format!("{}StartOptions", wf.rpc_method);
    let const_name = format!("{}_WORKFLOW_NAME", wf.rpc_method.to_shouty_snake_case());

    let _ = writeln!(
        out,
        "    /// Start `{}` and atomically deliver the `{}` signal.",
        wf.registered_name, sig.registered_name
    );
    let _ = writeln!(out, "    pub async fn {fn_name}(");
    let _ = writeln!(out, "        client: &temporal_runtime::TemporalClient,");
    if !sig.input_type.is_empty {
        let _ = writeln!(out, "        signal_input: {},", sig.input_type.rust_name());
    }
    if !wf.input_type.is_empty {
        let _ = writeln!(
            out,
            "        workflow_input: {},",
            wf.input_type.rust_name()
        );
    }
    let _ = writeln!(out, "        opts: {opts_struct},");
    let _ = writeln!(out, "    ) -> Result<{handle_struct}> {{");

    let signal_input_expr = if sig.input_type.is_empty {
        "&()".to_string()
    } else {
        "&signal_input".to_string()
    };
    let workflow_input_expr = if wf.input_type.is_empty {
        "&()".to_string()
    } else {
        "&workflow_input".to_string()
    };

    let _ = writeln!(
        out,
        "        let workflow_id = opts.workflow_id.clone().unwrap_or_else(|| {{"
    );
    let _ = writeln!(
        out,
        "            {}",
        id_fallback_call(wf, /* in_self_method: */ false)
    );
    let _ = writeln!(out, "        }});");
    if let Some(tq) = effective_task_queue(svc, wf) {
        let _ = writeln!(
            out,
            "        let task_queue = opts.task_queue.unwrap_or_else(|| \"{tq}\".to_string());"
        );
    } else {
        let _ = writeln!(
            out,
            "        let task_queue = opts.task_queue.expect(\"workflow has no proto-level task_queue; opts.task_queue is required\");"
        );
    }
    render_default_resolutions(out, wf, "        ");
    let _ = writeln!(
        out,
        "        let inner = temporal_runtime::signal_with_start_workflow_proto("
    );
    let _ = writeln!(out, "            client,");
    let _ = writeln!(out, "            {const_name},");
    let _ = writeln!(out, "            &workflow_id,");
    let _ = writeln!(out, "            &task_queue,");
    let _ = writeln!(out, "            {workflow_input_expr},");
    let _ = writeln!(out, "            \"{}\",", sig.registered_name);
    let _ = writeln!(out, "            {signal_input_expr},");
    let _ = writeln!(out, "            id_reuse_policy,");
    let _ = writeln!(out, "            execution_timeout,");
    let _ = writeln!(out, "            run_timeout,");
    let _ = writeln!(out, "            task_timeout,");
    let _ = writeln!(out, "        ).await?;");
    let _ = writeln!(out, "        Ok({handle_struct} {{ inner }})");
    let _ = writeln!(out, "    }}");
    let _ = writeln!(out);
}

fn render_update_with_start_fn(
    out: &mut String,
    svc: &ServiceModel,
    wf: &WorkflowModel,
    u: &UpdateModel,
) {
    let fn_name = format!("{}_with_start", u.rpc_method.to_snake_case());
    let handle_struct = format!("{}Handle", wf.rpc_method);
    let opts_struct = format!("{}StartOptions", wf.rpc_method);
    let const_name = format!("{}_WORKFLOW_NAME", wf.rpc_method.to_shouty_snake_case());

    let _ = writeln!(
        out,
        "    /// Start `{}` and atomically deliver the `{}` update.",
        wf.registered_name, u.registered_name
    );
    let _ = writeln!(out, "    pub async fn {fn_name}(");
    let _ = writeln!(out, "        client: &temporal_runtime::TemporalClient,");
    if !u.input_type.is_empty {
        let _ = writeln!(out, "        update_input: {},", u.input_type.rust_name());
    }
    if !wf.input_type.is_empty {
        let _ = writeln!(
            out,
            "        workflow_input: {},",
            wf.input_type.rust_name()
        );
    }
    let _ = writeln!(out, "        opts: {opts_struct},");
    let _ = writeln!(out, "        wait_policy: temporal_runtime::WaitPolicy,");
    if u.output_type.is_empty {
        let _ = writeln!(out, "    ) -> Result<{handle_struct}> {{");
    } else {
        let _ = writeln!(
            out,
            "    ) -> Result<({handle_struct}, {})> {{",
            u.output_type.rust_name()
        );
    }

    let update_input_expr = if u.input_type.is_empty {
        "&()".to_string()
    } else {
        "&update_input".to_string()
    };
    let workflow_input_expr = if wf.input_type.is_empty {
        "&()".to_string()
    } else {
        "&workflow_input".to_string()
    };

    let _ = writeln!(
        out,
        "        let workflow_id = opts.workflow_id.clone().unwrap_or_else(|| {{"
    );
    let _ = writeln!(
        out,
        "            {}",
        id_fallback_call(wf, /* in_self_method: */ false)
    );
    let _ = writeln!(out, "        }});");
    if let Some(tq) = effective_task_queue(svc, wf) {
        let _ = writeln!(
            out,
            "        let task_queue = opts.task_queue.unwrap_or_else(|| \"{tq}\".to_string());"
        );
    } else {
        let _ = writeln!(
            out,
            "        let task_queue = opts.task_queue.expect(\"workflow has no proto-level task_queue; opts.task_queue is required\");"
        );
    }
    render_default_resolutions(out, wf, "        ");
    // Output type is Empty → route through the `_unit` sibling, which
    // validates the canonical Empty payload server-side instead of decoding
    // it into a `()` (the typed `update_with_start_workflow_proto` requires
    // `O: TemporalProtoMessage`, which `()` cannot satisfy).
    if u.output_type.is_empty {
        let _ = writeln!(
            out,
            "        let inner = temporal_runtime::update_with_start_workflow_proto_unit::<{}, {}>(",
            wf.input_type.rust_name(),
            u.input_type.rust_name(),
        );
    } else {
        // Three explicit generics: workflow input (W), update input (U),
        // update output (O). All three appear in distinct argument positions
        // so they could in principle be inferred, but spelling them out keeps
        // generated code grep-able and immune to inference brittleness.
        let _ = writeln!(
            out,
            "        let (inner, update_result) = temporal_runtime::update_with_start_workflow_proto::<{}, {}, {}>(",
            wf.input_type.rust_name(),
            u.input_type.rust_name(),
            u.output_type.rust_name(),
        );
    }
    let _ = writeln!(out, "            client,");
    let _ = writeln!(out, "            {const_name},");
    let _ = writeln!(out, "            &workflow_id,");
    let _ = writeln!(out, "            &task_queue,");
    let _ = writeln!(out, "            {workflow_input_expr},");
    let _ = writeln!(out, "            \"{}\",", u.registered_name);
    let _ = writeln!(out, "            {update_input_expr},");
    let _ = writeln!(out, "            wait_policy,");
    let _ = writeln!(out, "            id_reuse_policy,");
    let _ = writeln!(out, "            execution_timeout,");
    let _ = writeln!(out, "            run_timeout,");
    let _ = writeln!(out, "            task_timeout,");
    let _ = writeln!(out, "        ).await?;");
    if u.output_type.is_empty {
        let _ = writeln!(out, "        Ok({handle_struct} {{ inner }})");
    } else {
        let _ = writeln!(
            out,
            "        Ok(({handle_struct} {{ inner }}, update_result))"
        );
    }
    let _ = writeln!(out, "    }}");
    let _ = writeln!(out);
}

fn effective_task_queue<'a>(svc: &'a ServiceModel, wf: &'a WorkflowModel) -> Option<&'a str> {
    wf.task_queue
        .as_deref()
        .or(svc.default_task_queue.as_deref())
}

fn duration_literal(d: std::time::Duration) -> String {
    let secs = d.as_secs();
    let nanos = d.subsec_nanos();
    if nanos == 0 {
        format!("Duration::from_secs({secs})")
    } else {
        format!("Duration::new({secs}, {nanos})")
    }
}

fn mod_name(svc: &ServiceModel) -> String {
    // jobs.v1 + JobService -> jobs_v1_job_service_temporal
    format!(
        "{}_{}_temporal",
        svc.package.replace('.', "_"),
        svc.service.to_snake_case()
    )
}

fn proto_module_path(package: &str) -> String {
    let mut p = String::from("crate");
    if package.is_empty() {
        return p;
    }
    for seg in package.split('.') {
        p.push_str("::");
        p.push_str(seg);
    }
    p
}

/// Phase 2: emit the per-service `<Service>Activities` async trait + per-
/// activity name consts. Only invoked when `activities=true` AND the service
/// has activity-annotated methods.
///
/// Trait method signature uses `impl Future<Output = Result<O>> + Send` rather
/// than `async fn` so the `Send` bound is explicit (the consumer's adapter to
/// `temporalio-sdk`'s `#[activities]` macro needs a Send future to
/// satisfy `tokio::task::spawn`-style boundaries).
fn render_activities_trait(out: &mut String, svc: &ServiceModel) {
    use heck::{ToShoutySnakeCase, ToSnakeCase};

    let trait_name = format!("{}Activities", svc.service);

    let _ = writeln!(out);
    let _ = writeln!(
        out,
        "    // ── Activities ────────────────────────────────────────────"
    );
    let _ = writeln!(
        out,
        "    // Phase 2 (activities=true): typed trait + name consts. Wire to"
    );
    let _ = writeln!(
        out,
        "    // your worker via temporalio-sdk's #[activities] macro;"
    );
    let _ = writeln!(
        out,
        "    // see temporal-proto-runtime-bridge README for the adapter pattern."
    );
    let _ = writeln!(out);

    for act in &svc.activities {
        let const_ident = format!("{}_ACTIVITY_NAME", act.rpc_method.to_shouty_snake_case());
        let _ = writeln!(
            out,
            "    pub const {const_ident}: &str = \"{}\";",
            act.registered_name
        );
    }

    let _ = writeln!(out);

    let _ = writeln!(out, "    pub trait {trait_name}: Send + Sync + 'static {{");
    for act in &svc.activities {
        let method_name = act.rpc_method.to_snake_case();
        let input_ty = if act.input_type.is_empty {
            "()".to_string()
        } else {
            act.input_type.rust_name().to_string()
        };
        let output_ty = if act.output_type.is_empty {
            "()".to_string()
        } else {
            act.output_type.rust_name().to_string()
        };
        let _ = writeln!(
            out,
            "        fn {method_name}(&self, ctx: temporal_runtime::ActivityContext, input: {input_ty}) -> impl ::std::future::Future<Output = Result<{output_ty}>> + Send;"
        );
    }
    let _ = writeln!(out, "    }}");
    let _ = writeln!(out);
    let fn_name = format!("register_{}_activities", svc.service.to_snake_case());
    let _ = writeln!(
        out,
        "    pub fn {fn_name}<I>(worker: &mut temporal_runtime::worker::Worker, impl_: I) -> &mut temporal_runtime::worker::Worker"
    );
    let _ = writeln!(out, "    where");
    let _ = writeln!(
        out,
        "        I: {trait_name} + temporal_runtime::worker::ActivityImplementer,"
    );
    let _ = writeln!(out, "    {{");
    let _ = writeln!(out, "        worker.register_activities(impl_)");
    let _ = writeln!(out, "    }}");
}

/// Worker workflow contracts emitted under `workflows=true`. The output is a
/// proto-derived trait per workflow plus a thin registration helper that
/// delegates to the SDK macro-generated `WorkflowImplementer`.
fn render_workflow_contracts(out: &mut String, svc: &ServiceModel) {
    let has_handlers =
        !svc.signals.is_empty() || !svc.queries.is_empty() || !svc.updates.is_empty();
    if has_handlers {
        render_workflow_handler_name_consts(out, svc);
    }

    let _ = writeln!(out);
    let _ = writeln!(
        out,
        "    // -- Workflow definitions --------------------------------"
    );
    let _ = writeln!(
        out,
        "    // workflows=true: typed proto contracts + registration helpers."
    );
    let _ = writeln!(
        out,
        "    // The consumer owns the temporalio-sdk #[workflow] body and"
    );
    let _ = writeln!(
        out,
        "    // implements the matching <Workflow>Definition trait on it."
    );
    let _ = writeln!(out);

    for wf in &svc.workflows {
        render_workflow_definition(out, svc, wf);
    }
}

fn render_workflow_definition(out: &mut String, svc: &ServiceModel, wf: &WorkflowModel) {
    let trait_name = format!("{}Definition", wf.rpc_method);
    let register_fn = format!("register_{}_workflow", wf.rpc_method.to_snake_case());
    let workflow_const = format!("{}_WORKFLOW_NAME", wf.rpc_method.to_shouty_snake_case());
    let task_queue_const = format!("{}_TASK_QUEUE", wf.rpc_method.to_shouty_snake_case());
    let input_ty = wf.input_type.rust_name();
    let output_ty = wf.output_type.rust_name();

    let _ = writeln!(out, "    pub trait {trait_name}: 'static {{");
    let _ = writeln!(out, "        type Input;");
    let _ = writeln!(out, "        type Output;");
    let _ = writeln!(
        out,
        "        const WORKFLOW_NAME: &'static str = self::{workflow_const};"
    );
    let _ = writeln!(
        out,
        "        const TASK_QUEUE: &'static str = self::{task_queue_const};"
    );
    if !wf.aliases.is_empty() {
        let aliases_const = format!("{}_WORKFLOW_ALIASES", wf.rpc_method.to_shouty_snake_case());
        let _ = writeln!(
            out,
            "        const WORKFLOW_ALIASES: &'static [&'static str] = self::{aliases_const};"
        );
    }

    for sref in &wf.attached_signals {
        if svc.signals.iter().any(|s| s.rpc_method == sref.rpc_method) {
            let ident = format!("{}_SIGNAL_NAME", sref.rpc_method.to_shouty_snake_case());
            let _ = writeln!(out, "        const {ident}: &'static str = self::{ident};");
        }
    }
    for qref in &wf.attached_queries {
        if svc.queries.iter().any(|q| q.rpc_method == qref.rpc_method) {
            let ident = format!("{}_QUERY_NAME", qref.rpc_method.to_shouty_snake_case());
            let _ = writeln!(out, "        const {ident}: &'static str = self::{ident};");
        }
    }
    for uref in &wf.attached_updates {
        if svc.updates.iter().any(|u| u.rpc_method == uref.rpc_method) {
            let ident = format!("{}_UPDATE_NAME", uref.rpc_method.to_shouty_snake_case());
            let _ = writeln!(out, "        const {ident}: &'static str = self::{ident};");
        }
    }

    let _ = writeln!(out, "    }}");
    let _ = writeln!(out);
    let _ = writeln!(
        out,
        "    pub fn {register_fn}<W>(worker: &mut temporal_runtime::worker::Worker) -> &mut temporal_runtime::worker::Worker"
    );
    let _ = writeln!(out, "    where");
    let _ = writeln!(
        out,
        "        W: temporal_runtime::worker::WorkflowImplementer + {trait_name}<Input = {input_ty}, Output = {output_ty}>,"
    );
    let _ = writeln!(out, "    {{");
    let _ = writeln!(out, "        worker.register_workflow::<W>()");
    let _ = writeln!(out, "    }}");
    let _ = writeln!(out);
}

/// Per-rpc signal/query/update name consts. Emitted under the existing
/// service module so a consumer's hand-rolled `#[workflow]` impl can reference
/// generated constants instead of string literals.
fn render_workflow_handler_name_consts(out: &mut String, svc: &ServiceModel) {
    use heck::ToShoutySnakeCase;

    let _ = writeln!(out);
    let _ = writeln!(
        out,
        "    // ── Workflow handler names ──────────────────────────────"
    );
    let _ = writeln!(
        out,
        "    // Phase 3.0 (workflows=true): per-rpc registration name consts."
    );
    let _ = writeln!(
        out,
        "    // Reference these from your hand-rolled #[workflow] setup so"
    );
    let _ = writeln!(
        out,
        "    // signal/query/update names stay in sync with the proto."
    );
    let _ = writeln!(out);

    for sig in &svc.signals {
        let ident = format!("{}_SIGNAL_NAME", sig.rpc_method.to_shouty_snake_case());
        let _ = writeln!(
            out,
            "    pub const {ident}: &str = \"{}\";",
            sig.registered_name
        );
    }
    for q in &svc.queries {
        let ident = format!("{}_QUERY_NAME", q.rpc_method.to_shouty_snake_case());
        let _ = writeln!(
            out,
            "    pub const {ident}: &str = \"{}\";",
            q.registered_name
        );
    }
    for u in &svc.updates {
        let ident = format!("{}_UPDATE_NAME", u.rpc_method.to_shouty_snake_case());
        let _ = writeln!(
            out,
            "    pub const {ident}: &str = \"{}\";",
            u.registered_name
        );
    }
}

/// Phase 4.0: per-service `<service>_cli` module. Emits the clap-derive
/// `Cli` + `Command` enum + per-workflow `Start<Workflow>Args` /
/// `Attach<Workflow>Args` structs.
///
/// Phase 4.0 is the parser structure only — no `Cli::run` impl. The dispatch
/// glue is deferred to Phase 4.1 once the JSON-input → proto-message
/// deserialization story is decided (pbjson? a `--input` file flag with
/// prost::Message::decode? Both?).
fn render_cli_module(out: &mut String, svc: &ServiceModel) {
    use heck::{ToPascalCase, ToSnakeCase};

    let cli_mod = format!("{}_cli", svc.service.to_snake_case());
    let proto_mod = proto_module_path(&svc.package);

    let _ = writeln!(out);
    let _ = writeln!(
        out,
        "/// Phase 4.0 CLI scaffold. clap-derive parser structure; dispatch"
    );
    let _ = writeln!(
        out,
        "/// is consumer-supplied until Phase 4.1 lands the `Cli::run` impl."
    );
    let _ = writeln!(out, "#[allow(clippy::all, unused_imports, dead_code)]");
    let _ = writeln!(out, "pub mod {cli_mod} {{");
    let _ = writeln!(out, "    use crate::temporal_runtime;");
    let _ = writeln!(out, "    use {proto_mod}::*;");
    let _ = writeln!(out);

    let _ = writeln!(out, "    #[derive(temporal_runtime::clap::Parser)]");
    let _ = writeln!(
        out,
        "    #[command(name = \"{}\", about = \"Generated Temporal CLI for {}.{}\")]",
        svc.service.to_snake_case(),
        svc.package,
        svc.service,
    );
    let _ = writeln!(out, "    pub struct Cli {{");
    let _ = writeln!(out, "        #[command(subcommand)]");
    let _ = writeln!(out, "        pub command: Command,");
    let _ = writeln!(out, "    }}");
    let _ = writeln!(out);

    let _ = writeln!(out, "    #[derive(temporal_runtime::clap::Subcommand)]");
    let _ = writeln!(out, "    pub enum Command {{");
    for wf in svc.workflows.iter().filter(|wf| !wf.cli_ignore) {
        let pascal = wf.rpc_method.to_pascal_case();
        let _ = writeln!(
            out,
            "        /// Start a new `{}` workflow.",
            wf.registered_name
        );
        let _ = writeln!(out, "        Start{pascal}(Start{pascal}Args),");
        let _ = writeln!(
            out,
            "        /// Attach to a running `{}` workflow by id.",
            wf.registered_name
        );
        let _ = writeln!(out, "        Attach{pascal}(Attach{pascal}Args),");
    }
    let _ = writeln!(out, "    }}");
    let _ = writeln!(out);

    for wf in svc.workflows.iter().filter(|wf| !wf.cli_ignore) {
        let pascal = wf.rpc_method.to_pascal_case();
        let _ = writeln!(out, "    #[derive(temporal_runtime::clap::Args)]");
        let _ = writeln!(out, "    pub struct Start{pascal}Args {{");
        let _ = writeln!(
            out,
            "        /// Path to a JSON file containing the workflow input."
        );
        let _ = writeln!(
            out,
            "        /// Format: prost-json (matching pbjson) of `{}`.",
            wf.input_type.full_name
        );
        let _ = writeln!(out, "        #[arg(long)]");
        let _ = writeln!(out, "        pub input_file: ::std::path::PathBuf,");
        let _ = writeln!(
            out,
            "        /// Override the workflow id (otherwise derived from the proto template / random)."
        );
        let _ = writeln!(out, "        #[arg(long)]");
        let _ = writeln!(out, "        pub workflow_id: Option<String>,");
        let _ = writeln!(
            out,
            "        /// Wait for the workflow to complete and print its result."
        );
        let _ = writeln!(out, "        #[arg(long)]");
        let _ = writeln!(out, "        pub wait: bool,");
        let _ = writeln!(out, "    }}");
        let _ = writeln!(out);

        let _ = writeln!(out, "    #[derive(temporal_runtime::clap::Args)]");
        let _ = writeln!(out, "    pub struct Attach{pascal}Args {{");
        let _ = writeln!(out, "        /// Workflow id to attach to.");
        let _ = writeln!(out, "        pub workflow_id: String,");
        let _ = writeln!(
            out,
            "        /// Wait for the workflow to complete and print its result."
        );
        let _ = writeln!(out, "        #[arg(long)]");
        let _ = writeln!(out, "        pub wait: bool,");
        let _ = writeln!(out, "    }}");
        let _ = writeln!(out);
    }

    let _ = writeln!(out, "}}");
}

