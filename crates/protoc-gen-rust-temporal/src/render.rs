//! `ServiceModel` -> Rust source emission.
//!
//! Output shape (`<package>_<service>_temporal` module):
//!
//! * One `<RPC>_WORKFLOW_NAME` constant per workflow.
//! * One `<Service>Client` struct holding a `temporal_runtime::TemporalClient`,
//!   plus one `<rpc>(input, opts) -> <Workflow>Handle` start method and one
//!   `<rpc>_handle(workflow_id)` attach method per workflow.
//! * One `<Workflow>StartOptions` per workflow.
//! * One `<Workflow>Handle` per workflow, exposing only the signals, queries,
//!   and updates wired in via `WorkflowOptions.{signal,query,update}`.
//! * One `<signal>_with_start` / `<update>_with_start` free function per
//!   start-flagged ref.
//!
//! Generated code refers to runtime helpers via `crate::temporal_runtime::*`
//! — the consumer crate provides that module. This mirrors the PoC's wiring
//! and lets the plugin stay agnostic to whichever Temporal SDK shape the
//! consumer pins.

use std::fmt::Write;

use heck::{ToShoutySnakeCase, ToSnakeCase};

use crate::model::{
    IdTemplateSegment, QueryModel, ServiceModel, SignalModel, UpdateModel, WaitPolicyKind,
    WorkflowModel,
};

/// Render the `<package>_<service>_temporal.rs` source for one service.
pub fn render(svc: &ServiceModel, options: &crate::options::RenderOptions) -> String {
    let mut out = String::new();
    let mod_name = mod_name(svc);
    let proto_mod = proto_module_path(&svc.package);
    let client_struct = format!("{}Client", svc.service);

    let _ = writeln!(
        out,
        "// Code generated by protoc-gen-rust-temporal. DO NOT EDIT."
    );
    let _ = writeln!(out, "// source: {}", svc.source_file);
    let _ = writeln!(out);
    let _ = writeln!(out, "#[allow(clippy::all, unused_imports, dead_code)]");
    let _ = writeln!(out, "pub mod {mod_name} {{");
    let _ = writeln!(out, "    use anyhow::Result;");
    let _ = writeln!(out, "    use std::time::Duration;");
    let _ = writeln!(out, "    use crate::temporal_runtime;");
    let _ = writeln!(out, "    use {proto_mod}::*;");
    let _ = writeln!(out);

    render_message_type_impls(&mut out, svc, options);
    render_constants(&mut out, svc);
    render_id_fns(&mut out, svc);
    render_client_struct(&mut out, svc, &client_struct);
    for wf in &svc.workflows {
        render_start_options(&mut out, wf);
        render_handle(&mut out, svc, wf);
    }
    render_with_start_functions(&mut out, svc);

    // Phase 2: activities trait + name consts. Only emitted when both the
    // `activities=true` plugin option is set AND the service has at least
    // one activity-annotated method.
    if options.activities && !svc.activities.is_empty() {
        render_activities_trait(&mut out, svc);
    }

    // Worker workflow contracts. Only emitted when `workflows=true` AND the
    // service has at least one workflow rpc. The consumer still writes the
    // SDK macro-bearing workflow body; this emit owns the proto-derived names,
    // task queues, typed contract, and registration helper.
    if options.workflows && !svc.workflows.is_empty() {
        render_workflow_contracts(&mut out, svc);
    }

    let _ = writeln!(out, "}}");

    // Phase 4.0: clap-derive Cli with per-workflow subcommands. Emitted at
    // file scope (sibling to the service module) so the consumer can drop
    // `<service>_cli::Cli::parse()` into a main.rs without an inner-mod
    // import dance. Only emitted when `cli=true` AND the service has at
    // least one workflow rpc whose `(temporal.v1.workflow).cli.ignore` flag
    // is unset — workflows opting out keep the CLI scaffold from referencing
    // workflows the consumer doesn't want exposed.
    let has_visible_cli_workflow = svc.workflows.iter().any(|wf| !wf.cli_ignore);
    // `(temporal.v1.cli).ignore = true` at the service level suppresses
    // the entire CLI module — overrides the per-workflow heuristic.
    let service_cli_ignore = svc.cli_options.as_ref().is_some_and(|c| c.ignore);
    if options.cli && has_visible_cli_workflow && !service_cli_ignore {
        render_cli_module(&mut out, svc);
    }

    out
}

/// Emit one `impl temporal_runtime::TemporalProtoMessage for <Ty>` per
/// distinct prost message type referenced by this service. The runtime
/// facade re-exports the trait from `temporal-proto-runtime`, so this is
/// what lets `start_workflow_proto::<I>` / `signal_proto::<I>` etc. resolve
/// at the call site. `google.protobuf.Empty` types are skipped — the
/// `_empty` runtime variants take no payload generic.
fn render_message_type_impls(
    out: &mut String,
    svc: &ServiceModel,
    options: &crate::options::RenderOptions,
) {
    use std::collections::BTreeMap;

    let mut by_rust_name: BTreeMap<String, String> = BTreeMap::new();
    let mut record = |pt: &crate::model::ProtoType| {
        if pt.is_empty {
            return;
        }
        by_rust_name
            .entry(pt.rust_name().to_string())
            .or_insert_with(|| pt.full_name.clone());
    };

    for wf in &svc.workflows {
        record(&wf.input_type);
        record(&wf.output_type);
    }
    for s in &svc.signals {
        record(&s.input_type);
        record(&s.output_type);
    }
    for q in &svc.queries {
        record(&q.input_type);
        record(&q.output_type);
    }
    for u in &svc.updates {
        record(&u.input_type);
        record(&u.output_type);
    }
    if options.activities {
        for a in &svc.activities {
            record(&a.input_type);
            record(&a.output_type);
        }
    }

    if by_rust_name.is_empty() {
        return;
    }

    for (rust_name, full_name) in &by_rust_name {
        let _ = writeln!(
            out,
            "    impl temporal_runtime::TemporalProtoMessage for {rust_name} {{"
        );
        let _ = writeln!(
            out,
            "        const MESSAGE_TYPE: &'static str = \"{full_name}\";"
        );
        let _ = writeln!(out, "    }}");
    }
    let _ = writeln!(out);
}

fn render_constants(out: &mut String, svc: &ServiceModel) {
    for wf in &svc.workflows {
        let const_name = format!("{}_WORKFLOW_NAME", wf.rpc_method.to_shouty_snake_case());
        let _ = writeln!(
            out,
            "    pub const {const_name}: &str = \"{}\";",
            wf.registered_name
        );
        // Per-workflow input/output proto type FQN consts. Lets
        // tooling (codecs, payload routers, cross-language test
        // harnesses) look up the proto message name without
        // re-traversing the descriptor pool. Empty sides land as
        // `"google.protobuf.Empty"` — preserved verbatim from
        // `ProtoType.full_name`.
        let input_type_const = format!("{}_INPUT_TYPE", wf.rpc_method.to_shouty_snake_case());
        let _ = writeln!(
            out,
            "    pub const {input_type_const}: &str = \"{}\";",
            wf.input_type.full_name
        );
        let output_type_const = format!("{}_OUTPUT_TYPE", wf.rpc_method.to_shouty_snake_case());
        let _ = writeln!(
            out,
            "    pub const {output_type_const}: &str = \"{}\";",
            wf.output_type.full_name
        );
        if let Some(tq) = effective_task_queue(svc, wf) {
            let tq_const = format!("{}_TASK_QUEUE", wf.rpc_method.to_shouty_snake_case());
            let _ = writeln!(out, "    pub const {tq_const}: &str = \"{tq}\";");
        }
        // Verbatim id template source — preserved for tooling that
        // needs the original template (debug inspectors, doc
        // generators) without reconstructing it from the parsed
        // segments.
        if let Some(tpl) = wf.id_template_source.as_deref() {
            let id_const = format!(
                "{}_WORKFLOW_ID_TEMPLATE",
                wf.rpc_method.to_shouty_snake_case()
            );
            let _ = writeln!(
                out,
                "    pub const {id_const}: &str = \"{}\";",
                tpl.escape_default()
            );
        }
        // Workflow aliases from `(temporal.v1.workflow).aliases`. Only emitted
        // when at least one alias is declared so existing goldens stay clean.
        // Consumers register the workflow under each name; the Go plugin does
        // the equivalent inside its worker registration helper.
        if !wf.aliases.is_empty() {
            let aliases_const =
                format!("{}_WORKFLOW_ALIASES", wf.rpc_method.to_shouty_snake_case());
            let joined = wf
                .aliases
                .iter()
                .map(|a| format!("\"{}\"", a.escape_default()))
                .collect::<Vec<_>>()
                .join(", ");
            let _ = writeln!(out, "    pub const {aliases_const}: &[&str] = &[{joined}];");
        }
        // Per-workflow attached-handler name lists. Each lists the
        // *registered* names (the same strings consumers see on the
        // wire) of the signals / queries / updates this workflow ref'd
        // via `WorkflowOptions.{signal,query,update}[]`. Only emits
        // when the list is non-empty so workflows with no attached
        // handlers don't bloat the surface. Resolves both same-service
        // refs (look up SignalModel et al on `svc`) and cross-service
        // refs (registered_name lives on `cross_service.registered_name`).
        emit_attached_handler_names(out, svc, wf);
    }
    if !svc.workflows.is_empty() {
        let _ = writeln!(out);
    }
    // Per-rpc input/output type FQN consts for signals, queries,
    // updates, and activities — same shape as the workflow `_INPUT_TYPE`
    // / `_OUTPUT_TYPE` consts above. Lets consumer tooling enumerate
    // proto type names for every rpc kind without descriptor-pool
    // round-trips.
    for sig in &svc.signals {
        let in_const = format!(
            "{}_SIGNAL_INPUT_TYPE",
            sig.rpc_method.to_shouty_snake_case()
        );
        let _ = writeln!(
            out,
            "    pub const {in_const}: &str = \"{}\";",
            sig.input_type.full_name
        );
    }
    for q in &svc.queries {
        let in_const = format!("{}_QUERY_INPUT_TYPE", q.rpc_method.to_shouty_snake_case());
        let out_const = format!("{}_QUERY_OUTPUT_TYPE", q.rpc_method.to_shouty_snake_case());
        let _ = writeln!(
            out,
            "    pub const {in_const}: &str = \"{}\";",
            q.input_type.full_name
        );
        let _ = writeln!(
            out,
            "    pub const {out_const}: &str = \"{}\";",
            q.output_type.full_name
        );
    }
    for u in &svc.updates {
        let in_const = format!("{}_UPDATE_INPUT_TYPE", u.rpc_method.to_shouty_snake_case());
        let out_const = format!("{}_UPDATE_OUTPUT_TYPE", u.rpc_method.to_shouty_snake_case());
        let _ = writeln!(
            out,
            "    pub const {in_const}: &str = \"{}\";",
            u.input_type.full_name
        );
        let _ = writeln!(
            out,
            "    pub const {out_const}: &str = \"{}\";",
            u.output_type.full_name
        );
        // Verbatim id template, parallel of the workflow-side const.
        if let Some(tpl) = u.id_template_source.as_deref() {
            let id_const = format!("{}_UPDATE_ID_TEMPLATE", u.rpc_method.to_shouty_snake_case());
            let _ = writeln!(
                out,
                "    pub const {id_const}: &str = \"{}\";",
                tpl.escape_default()
            );
        }
    }
    for act in &svc.activities {
        let in_const = format!(
            "{}_ACTIVITY_INPUT_TYPE",
            act.rpc_method.to_shouty_snake_case()
        );
        let out_const = format!(
            "{}_ACTIVITY_OUTPUT_TYPE",
            act.rpc_method.to_shouty_snake_case()
        );
        let _ = writeln!(
            out,
            "    pub const {in_const}: &str = \"{}\";",
            act.input_type.full_name
        );
        let _ = writeln!(
            out,
            "    pub const {out_const}: &str = \"{}\";",
            act.output_type.full_name
        );
        // Per-activity task_queue const, parallel of the per-workflow
        // `<RPC>_TASK_QUEUE`. Only emits when the proto declares one
        // (via the activity's `default_options.task_queue`).
        if let Some(tq) = act
            .default_options
            .as_ref()
            .and_then(|s| s.task_queue.as_deref())
        {
            let tq_const = format!(
                "{}_ACTIVITY_TASK_QUEUE",
                act.rpc_method.to_shouty_snake_case()
            );
            let _ = writeln!(
                out,
                "    pub const {tq_const}: &str = \"{}\";",
                tq.escape_default()
            );
        }
    }
    if !svc.signals.is_empty()
        || !svc.queries.is_empty()
        || !svc.updates.is_empty()
        || !svc.activities.is_empty()
    {
        let _ = writeln!(out);
    }
}

/// Emit `<RPC>_ATTACHED_SIGNAL_NAMES` / `_QUERY_NAMES` / `_UPDATE_NAMES`
/// `&'static [&'static str]` consts for the given workflow when the
/// corresponding attached-ref list is non-empty. Each entry is the
/// *registered* (Temporal-wire) name, resolved against same-service
/// models or cross-service target metadata captured at parse.
fn emit_attached_handler_names(
    out: &mut String,
    svc: &ServiceModel,
    wf: &crate::model::WorkflowModel,
) {
    use heck::ToShoutySnakeCase;
    let resolve_signal = |sref: &crate::model::SignalRef| -> Option<String> {
        if let Some(target) = sref.cross_service.as_ref() {
            return Some(target.registered_name.clone());
        }
        svc.signals
            .iter()
            .find(|s| s.rpc_method == sref.rpc_method)
            .map(|s| s.registered_name.clone())
    };
    let resolve_query = |qref: &crate::model::QueryRef| -> Option<String> {
        if let Some(target) = qref.cross_service.as_ref() {
            return Some(target.registered_name.clone());
        }
        svc.queries
            .iter()
            .find(|q| q.rpc_method == qref.rpc_method)
            .map(|q| q.registered_name.clone())
    };
    let resolve_update = |uref: &crate::model::UpdateRef| -> Option<String> {
        if let Some(target) = uref.cross_service.as_ref() {
            return Some(target.registered_name.clone());
        }
        svc.updates
            .iter()
            .find(|u| u.rpc_method == uref.rpc_method)
            .map(|u| u.registered_name.clone())
    };
    let emit = |out: &mut String, suffix: &str, names: &[String]| {
        if names.is_empty() {
            return;
        }
        let ident = format!(
            "{}_ATTACHED_{}",
            wf.rpc_method.to_shouty_snake_case(),
            suffix
        );
        let joined = names
            .iter()
            .map(|n| format!("\"{}\"", n.escape_default()))
            .collect::<Vec<_>>()
            .join(", ");
        let _ = writeln!(
            out,
            "    pub const {ident}: &'static [&'static str] = &[{joined}];"
        );
    };
    let sig_names: Vec<String> = wf
        .attached_signals
        .iter()
        .filter_map(resolve_signal)
        .collect();
    let q_names: Vec<String> = wf
        .attached_queries
        .iter()
        .filter_map(resolve_query)
        .collect();
    let u_names: Vec<String> = wf
        .attached_updates
        .iter()
        .filter_map(resolve_update)
        .collect();
    emit(out, "SIGNAL_NAMES", &sig_names);
    emit(out, "QUERY_NAMES", &q_names);
    emit(out, "UPDATE_NAMES", &u_names);
}

/// Emit one private `<wf>_id` function per workflow that has an `id`
/// template, materialising the template into a `format!` against the
/// input message's fields at codegen time. The start path (and the
/// `<signal>_with_start` / `<update>_with_start` free functions) call
/// this instead of a generic runtime template helper — there's no
/// runtime template engine to maintain and the field lookups are
/// statically type-checked.
fn render_id_fns(out: &mut String, svc: &ServiceModel) {
    for wf in &svc.workflows {
        let Some(segments) = wf.id_expression.as_ref() else {
            continue;
        };
        let fn_name = format!("{}_id", wf.rpc_method.to_snake_case());
        emit_id_fn(out, &fn_name, segments, &wf.input_type);
    }
    // R5: `UpdateOptions.id` is a workflow-id template resolved against
    // the *update* input, used by the `<update>_by_template` client
    // convenience. Same compilation strategy as workflow id templates,
    // just bound to the update's input descriptor.
    for u in &svc.updates {
        let Some(segments) = u.id_expression.as_ref() else {
            continue;
        };
        let fn_name = format!("{}_workflow_id", u.rpc_method.to_snake_case());
        emit_id_fn(out, &fn_name, segments, &u.input_type);
    }
}

/// Shared emit body for workflow-id and update-target-id derivation
/// helpers. `input_ty` controls the signature; if it's Empty the fn
/// takes no args (validation guarantees template segments can't
/// reference fields on Empty).
fn emit_id_fn(
    out: &mut String,
    fn_name: &str,
    segments: &[IdTemplateSegment],
    input_ty: &crate::model::ProtoType,
) {
    let (fmt, args) = compile_id_template(segments);
    if input_ty.is_empty {
        let _ = writeln!(out, "    fn {fn_name}() -> String {{");
        if fmt.is_empty() {
            let _ = writeln!(out, "        String::new()");
        } else {
            let _ = writeln!(out, "        \"{fmt}\".to_string()");
        }
        let _ = writeln!(out, "    }}");
        let _ = writeln!(out);
        return;
    }
    let ty = input_ty.rust_name();
    let _ = writeln!(out, "    fn {fn_name}(input: &{ty}) -> String {{");
    if args.is_empty() {
        let _ = writeln!(out, "        let _ = input;");
        let _ = writeln!(out, "        \"{fmt}\".to_string()");
    } else {
        // Substitute the proto-id template via `format!`, then guard
        // against an empty result. Field refs that resolve to empty
        // strings at runtime (proto3 string defaults) would produce
        // a workflow id Temporal then rejects with an opaque server
        // error — surface the failure here so the bug is locally
        // fixable. The assert message can't reuse `fmt` directly:
        // any `{}` from field substitution would be re-interpreted
        // as a format placeholder. Double-brace-escape so the
        // template renders verbatim in the panic message.
        let escaped_fmt = fmt.replace('{', "{{").replace('}', "}}");
        let _ = writeln!(
            out,
            "        let id = format!(\"{fmt}\", {});",
            args.join(", ")
        );
        let _ = writeln!(
            out,
            "        assert!(!id.is_empty(), \"workflow id template `{escaped_fmt}` resolved to an empty string at runtime — check that every referenced input field has a non-empty value\");"
        );
        let _ = writeln!(out, "        id");
    }
    let _ = writeln!(out, "    }}");
    let _ = writeln!(out);
}

/// Pick the call site that produces a workflow id when the caller did
/// not supply one. With a proto-declared `id` template we route through
/// the codegen-emitted `<wf>_id(...)` function; without one we fall
/// back to `temporal_runtime::random_workflow_id()`.
///
/// `in_self_method` is true inside `<Service>Client::<rpc>` (where the
/// workflow input is named `input`) and false inside the
/// `<signal>_with_start` / `<update>_with_start` free functions (where
/// the workflow input is named `workflow_input`).
fn id_fallback_call(wf: &WorkflowModel, in_self_method: bool) -> String {
    if wf.id_expression.is_none() {
        return "temporal_runtime::random_workflow_id()".to_string();
    }
    let fn_name = format!("{}_id", wf.rpc_method.to_snake_case());
    if wf.input_type.is_empty {
        // Validated in parse.rs: an Empty workflow input cannot host a
        // Field segment, so the id fn takes no arguments.
        return format!("{fn_name}()");
    }
    if in_self_method {
        format!("{fn_name}(&input)")
    } else {
        format!("{fn_name}(&workflow_input)")
    }
}

/// Walk template segments into a `format!` string + arg list. Literal `{`
/// and `}` characters in the template are doubled so they survive the
/// `format!` parse.
fn compile_id_template(segments: &[IdTemplateSegment]) -> (String, Vec<String>) {
    let mut fmt = String::new();
    let mut args = Vec::new();
    for seg in segments {
        match seg {
            IdTemplateSegment::Literal(s) => {
                fmt.push_str(&s.replace('{', "{{").replace('}', "}}"));
            }
            IdTemplateSegment::Field(rust_name) => {
                fmt.push_str("{}");
                args.push(format!("input.{rust_name}"));
            }
        }
    }
    (fmt, args)
}

/// Emit service-level identity + aggregate constants on the
/// `<Service>Client` impl. Includes the package / service-name /
/// fully-qualified-service-name identity triple, then the
/// `WORKFLOW_NAMES` / `SIGNAL_NAMES` / `QUERY_NAMES` / `UPDATE_NAMES`
/// / `ACTIVITY_NAMES` aggregates. Empty aggregate kinds are omitted.
fn render_service_name_aggregates(out: &mut String, svc: &ServiceModel) {
    // Identity triple — useful for tooling that needs the proto
    // namespace at runtime (codecs, registries, JSON dispatch tables).
    let _ = writeln!(
        out,
        "        pub const PACKAGE: &'static str = \"{}\";",
        svc.package.escape_default()
    );
    let _ = writeln!(
        out,
        "        pub const SERVICE_NAME: &'static str = \"{}\";",
        svc.service.escape_default()
    );
    let fqn = if svc.package.is_empty() {
        svc.service.clone()
    } else {
        format!("{}.{}", svc.package, svc.service)
    };
    let _ = writeln!(
        out,
        "        pub const FULLY_QUALIFIED_SERVICE_NAME: &'static str = \"{}\";",
        fqn.escape_default()
    );
    // Source proto path, as protoc saw it. Lets tooling correlate
    // generated code back to the input proto without parsing build
    // outputs.
    let _ = writeln!(
        out,
        "        pub const SOURCE_FILE: &'static str = \"{}\";",
        svc.source_file.escape_default()
    );
    // Plugin version embedded at codegen time. Lets tooling identify
    // which `protoc-gen-rust-temporal` release produced the file —
    // useful when debugging "code doesn't compile, must be a generator
    // bug" reports.
    let _ = writeln!(
        out,
        "        pub const GENERATED_BY_PLUGIN_VERSION: &'static str = \"protoc-gen-rust-temporal {}\";",
        env!("CARGO_PKG_VERSION")
    );
    let emit = |out: &mut String, ident: &str, names: &[&str]| {
        if names.is_empty() {
            return;
        }
        let joined = names
            .iter()
            .map(|n| format!("\"{}\"", n.escape_default()))
            .collect::<Vec<_>>()
            .join(", ");
        let _ = writeln!(
            out,
            "        pub const {ident}: &'static [&'static str] = &[{joined}];"
        );
    };
    let wf_names: Vec<&str> = svc
        .workflows
        .iter()
        .map(|w| w.registered_name.as_str())
        .collect();
    let sig_names: Vec<&str> = svc
        .signals
        .iter()
        .map(|s| s.registered_name.as_str())
        .collect();
    let q_names: Vec<&str> = svc
        .queries
        .iter()
        .map(|q| q.registered_name.as_str())
        .collect();
    let u_names: Vec<&str> = svc
        .updates
        .iter()
        .map(|u| u.registered_name.as_str())
        .collect();
    let act_names: Vec<&str> = svc
        .activities
        .iter()
        .map(|a| a.registered_name.as_str())
        .collect();
    emit(out, "WORKFLOW_NAMES", &wf_names);
    emit(out, "SIGNAL_NAMES", &sig_names);
    emit(out, "QUERY_NAMES", &q_names);
    emit(out, "UPDATE_NAMES", &u_names);
    emit(out, "ACTIVITY_NAMES", &act_names);
    // Identity triple always emits, so the trailing newline is always
    // needed to separate the const block from the `new()` ctor below.
    let _ = writeln!(out);
}

fn render_client_struct(out: &mut String, svc: &ServiceModel, client_struct: &str) {
    let _ = writeln!(out, "    pub struct {client_struct} {{");
    let _ = writeln!(out, "        client: temporal_runtime::TemporalClient,");
    let _ = writeln!(out, "    }}");
    let _ = writeln!(out);

    // Manual `Debug` impl — bridge `TemporalClient` doesn't derive
    // Debug (the inner SDK client is opaque), so derive on the
    // wrapper isn't an option. Print only the structured identity
    // (package, service, plugin version) rather than the inner
    // client. Useful for `tracing::info!(?client, ...)` style
    // logging without dumping connection internals.
    let _ = writeln!(out, "    impl ::std::fmt::Debug for {client_struct} {{");
    let _ = writeln!(
        out,
        "        fn fmt(&self, f: &mut ::std::fmt::Formatter<'_>) -> ::std::fmt::Result {{"
    );
    let _ = writeln!(out, "            f.debug_struct(\"{client_struct}\")");
    let _ = writeln!(out, "                .field(\"package\", &Self::PACKAGE)");
    let _ = writeln!(
        out,
        "                .field(\"service\", &Self::SERVICE_NAME)"
    );
    let _ = writeln!(
        out,
        "                .field(\"plugin_version\", &Self::GENERATED_BY_PLUGIN_VERSION)"
    );
    let _ = writeln!(out, "                .finish_non_exhaustive()");
    let _ = writeln!(out, "        }}");
    let _ = writeln!(out, "    }}");
    let _ = writeln!(out);

    let _ = writeln!(out, "    impl {client_struct} {{");
    // Service-level aggregate name consts — let tooling enumerate
    // every workflow / signal / query / update / activity registered
    // by this service without re-implementing the snake-case + name
    // resolution logic the plugin does at codegen.
    render_service_name_aggregates(out, svc);
    let _ = writeln!(
        out,
        "        pub fn new(client: temporal_runtime::TemporalClient) -> Self {{"
    );
    let _ = writeln!(out, "            Self {{ client }}");
    let _ = writeln!(out, "        }}");
    let _ = writeln!(out);
    // Convenience constructor — connects to Temporal and wraps the
    // resulting client in one call. Lets `main` skip the boilerplate
    // of `temporal_runtime::connect(...).await?` followed by
    // `<Service>Client::new(...)`. Errors surface verbatim from the
    // bridge.
    let _ = writeln!(
        out,
        "        pub async fn connect(url: &str, namespace: &str) -> Result<Self> {{"
    );
    let _ = writeln!(
        out,
        "            let client = temporal_runtime::connect(url, namespace).await?;"
    );
    let _ = writeln!(out, "            Ok(Self::new(client))");
    let _ = writeln!(out, "        }}");
    let _ = writeln!(out);
    let _ = writeln!(
        out,
        "        pub fn inner(&self) -> &temporal_runtime::TemporalClient {{"
    );
    let _ = writeln!(out, "            &self.client");
    let _ = writeln!(out, "        }}");
    let _ = writeln!(out);

    for wf in &svc.workflows {
        render_client_workflow_methods(out, svc, wf);
    }

    // R4: client-level signal-by-id helpers. One method per signal rpc, so
    // callers with a workflow id can send a signal without `<rpc>_handle`
    // round-trip. Skips signals that aren't attached to any workflow on
    // this service (defensive — validate.rs already requires each
    // `WorkflowOptions.signal[].ref` to resolve, so the set of attached
    // signals matches the set of signal rpcs in practice).
    let attached_signal_names: std::collections::HashSet<&str> = svc
        .workflows
        .iter()
        .flat_map(|wf| wf.attached_signals.iter())
        .map(|s| s.rpc_method.as_str())
        .collect();
    for sig in &svc.signals {
        if !attached_signal_names.contains(sig.rpc_method.as_str()) {
            continue;
        }
        render_client_signal_method(out, sig);
    }

    // R4: client-level query-by-id. Mirrors render_query_method on the
    // Handle but the by-id variant attaches a handle internally first.
    let attached_query_names: std::collections::HashSet<&str> = svc
        .workflows
        .iter()
        .flat_map(|wf| wf.attached_queries.iter())
        .map(|q| q.rpc_method.as_str())
        .collect();
    for q in &svc.queries {
        if !attached_query_names.contains(q.rpc_method.as_str()) {
            continue;
        }
        render_client_query_method(out, q);
    }

    // R4: client-level update-by-id. Same Empty matrix as on the Handle
    // plus a `wait_policy` arg.
    let attached_update_names: std::collections::HashSet<&str> = svc
        .workflows
        .iter()
        .flat_map(|wf| wf.attached_updates.iter())
        .map(|u| u.rpc_method.as_str())
        .collect();
    for u in &svc.updates {
        if !attached_update_names.contains(u.rpc_method.as_str()) {
            continue;
        }
        render_client_update_method(out, u);
        // R5: `<update>_by_template` derives the parent workflow id from
        // `UpdateOptions.id` against the update input, then forwards to
        // the by-id variant above. Only emitted when the proto declares
        // the template — keeps the surface honest.
        if u.id_expression.is_some() {
            render_client_update_by_template_method(out, u);
        }
    }

    let _ = writeln!(out, "    }}");
    let _ = writeln!(out);
}

/// `<update>_by_template`: derives the parent workflow id from
/// `UpdateOptions.id` against the update input, then forwards to the
/// by-id update method. Only emitted when the proto declares the
/// template. Empty-output variant returns `Result<()>`; non-Empty
/// returns the typed output.
/// R5: every update method takes `wait_policy: Option<temporal_runtime::WaitPolicy>`
/// and resolves it at the call site against the proto-declared default
/// (`UpdateOptions.wait_for_stage` / deprecated `wait_policy`) — falling
/// back to `Completed` when the proto leaves it unset, matching the SDK's
/// previous mandatory-arg behaviour at the legacy emit.
fn wait_policy_resolution_line(u: &UpdateModel, ind: &str) -> String {
    let default_variant = u
        .default_wait_policy
        .map(WaitPolicyKind::rust_variant)
        .unwrap_or("Completed");
    format!(
        "{ind}let wait_policy = wait_policy.unwrap_or(temporal_runtime::WaitPolicy::{default_variant});\n"
    )
}

fn render_client_update_by_template_method(out: &mut String, u: &UpdateModel) {
    let method_snake = u.rpc_method.to_snake_case();
    let by_id = method_snake.clone();
    let by_template = format!("{method_snake}_by_template");
    let out_ty = u.output_type.rust_name();
    let _ = writeln!(
        out,
        "        /// Run the `{}` update, deriving the parent workflow id from",
        u.registered_name
    );
    let _ = writeln!(
        out,
        "        /// `(temporal.v1.update).id` against the update input."
    );
    let id_fn = format!("{method_snake}_workflow_id");
    if u.input_type.is_empty {
        // Empty-input update with a template — template must have no Field
        // segments (parse-time validation). Emit a no-arg derivation call.
        let _ = writeln!(
            out,
            "        pub async fn {by_template}(&self, wait_policy: Option<temporal_runtime::WaitPolicy>) -> Result<{out_ty}> {{"
        );
        let _ = writeln!(out, "            let workflow_id = {id_fn}();");
        let _ = writeln!(
            out,
            "            self.{by_id}(workflow_id, wait_policy).await"
        );
    } else {
        let in_ty = u.input_type.rust_name();
        let _ = writeln!(
            out,
            "        pub async fn {by_template}(&self, input: {in_ty}, wait_policy: Option<temporal_runtime::WaitPolicy>) -> Result<{out_ty}> {{"
        );
        let _ = writeln!(out, "            let workflow_id = {id_fn}(&input);");
        // The by-id update takes the same args for both Empty- and
        // non-Empty-output variants — the return type differs in the
        // signature, not the call.
        let _ = writeln!(
            out,
            "            self.{by_id}(workflow_id, input, wait_policy).await"
        );
    }
    let _ = writeln!(out, "        }}");
    let _ = writeln!(out);
}

