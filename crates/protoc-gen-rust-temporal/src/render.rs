fn render_client_workflow_methods(out: &mut String, svc: &ServiceModel, wf: &WorkflowModel) {
    let method_snake = wf.rpc_method.to_snake_case();
    let handle_struct = format!("{}Handle", wf.rpc_method);
    let opts_struct = format!("{}StartOptions", wf.rpc_method);
    let const_name = format!("{}_WORKFLOW_NAME", wf.rpc_method.to_shouty_snake_case());

    let _ = writeln!(
        out,
        "        /// Start a new `{}` workflow.",
        wf.registered_name
    );
    let _ = writeln!(out, "        pub async fn {method_snake}(");
    let _ = writeln!(out, "            &self,");
    if !wf.input_type.is_empty {
        let _ = writeln!(out, "            input: {},", wf.input_type.rust_name());
    }
    let _ = writeln!(out, "            opts: {opts_struct},");
    let _ = writeln!(out, "        ) -> Result<{handle_struct}> {{");
    render_start_body(
        out,
        svc,
        wf,
        &const_name,
        /* leading_indent: */ "            ",
    );
    let _ = writeln!(out, "            Ok({handle_struct} {{ inner }})");
    let _ = writeln!(out, "        }}");
    let _ = writeln!(out);

    let _ = writeln!(
        out,
        "        /// Attach to a running `{}` workflow by id.",
        wf.registered_name
    );
    let _ = writeln!(
        out,
        "        pub fn {method_snake}_handle(&self, workflow_id: impl Into<String>) -> {handle_struct} {{"
    );
    let _ = writeln!(out, "            {handle_struct} {{");
    let _ = writeln!(
        out,
        "                inner: temporal_runtime::attach_handle(&self.client, workflow_id.into()),"
    );
    let _ = writeln!(out, "            }}");
    let _ = writeln!(out, "        }}");
    let _ = writeln!(out);
}

/// Emit the body of the regular start method: resolves workflow_id +
/// task_queue and dispatches `start_workflow_proto`. `leading_indent` is the
/// whitespace prefix applied to each emitted line (12 spaces inside an
/// `impl` method, 8 spaces inside a free function).
fn render_start_body(
    out: &mut String,
    svc: &ServiceModel,
    wf: &WorkflowModel,
    const_name: &str,
    leading_indent: &str,
) {
    let ind = leading_indent;
    let _ = writeln!(
        out,
        "{ind}let workflow_id = opts.workflow_id.unwrap_or_else(|| {{"
    );
    let _ = writeln!(
        out,
        "{ind}    {}",
        id_fallback_call(wf, /* in_self_method: */ true)
    );
    let _ = writeln!(out, "{ind}}});");

    if let Some(tq) = effective_task_queue(svc, wf) {
        let _ = writeln!(
            out,
            "{ind}let task_queue = opts.task_queue.unwrap_or_else(|| \"{tq}\".to_string());"
        );
    } else {
        let _ = writeln!(
            out,
            "{ind}let task_queue = opts.task_queue.expect(\"workflow has no proto-level task_queue; opts.task_queue is required\");"
        );
    }

    render_default_resolutions(out, wf, ind);

    if wf.input_type.is_empty {
        // Empty workflow input — route to a separate runtime function that
        // skips the payload arg. Avoids `&()` not impl'ing
        // TemporalProtoMessage at the call site.
        let _ = writeln!(
            out,
            "{ind}let inner = temporal_runtime::start_workflow_proto_empty("
        );
        let _ = writeln!(out, "{ind}    &self.client,");
        let _ = writeln!(out, "{ind}    {const_name},");
        let _ = writeln!(out, "{ind}    &workflow_id,");
        let _ = writeln!(out, "{ind}    &task_queue,");
    } else {
        let _ = writeln!(
            out,
            "{ind}let inner = temporal_runtime::start_workflow_proto("
        );
        let _ = writeln!(out, "{ind}    &self.client,");
        let _ = writeln!(out, "{ind}    {const_name},");
        let _ = writeln!(out, "{ind}    &workflow_id,");
        let _ = writeln!(out, "{ind}    &task_queue,");
        let _ = writeln!(out, "{ind}    &input,");
    }
    let _ = writeln!(out, "{ind}    id_reuse_policy,");
    let _ = writeln!(out, "{ind}    id_conflict_policy,");
    let _ = writeln!(out, "{ind}    execution_timeout,");
    let _ = writeln!(out, "{ind}    run_timeout,");
    let _ = writeln!(out, "{ind}    task_timeout,");
    let _ = writeln!(out, "{ind}    enable_eager_workflow_start,");
    let _ = writeln!(out, "{ind}    retry_policy,");
    let _ = writeln!(out, "{ind}    search_attributes,");
    let _ = writeln!(out, "{ind}).await?;");
}

/// Emit `let <field> = opts.<field>.or(Some(<default>));` for every
/// `WorkflowOptions` field that has a schema-declared default, and a
/// plain rebind for the rest. This folds the proto-level defaults into
/// the start call so callers who leave a `StartOptions` field as `None`
/// still get the workflow's declared default — `default_*()` helpers
/// on `StartOptions` are no longer enough on their own because the start
/// path used `opts.<field>` directly.
fn render_default_resolutions(out: &mut String, wf: &WorkflowModel, ind: &str) {
    let id_reuse_default = wf.id_reuse_policy.map(|p| {
        format!(
            "temporal_runtime::WorkflowIdReusePolicy::{}",
            p.rust_variant()
        )
    });
    let id_conflict_default = wf.id_conflict_policy.map(|p| {
        format!(
            "temporal_runtime::WorkflowIdConflictPolicy::{}",
            p.rust_variant()
        )
    });
    let resolutions: [(&'static str, Option<String>); 5] = [
        ("id_reuse_policy", id_reuse_default),
        ("id_conflict_policy", id_conflict_default),
        (
            "execution_timeout",
            wf.execution_timeout.map(duration_literal),
        ),
        ("run_timeout", wf.run_timeout.map(duration_literal)),
        ("task_timeout", wf.task_timeout.map(duration_literal)),
    ];
    for (field, default) in resolutions {
        match default {
            Some(value) => {
                let _ = writeln!(out, "{ind}let {field} = opts.{field}.or(Some({value}));");
            }
            None => {
                let _ = writeln!(out, "{ind}let {field} = opts.{field};");
            }
        }
    }
    // Eager start has a `bool` upstream default, so we resolve to a bare
    // bool (not Option) and bake the proto-declared default in directly.
    let eager_default = if wf.enable_eager_workflow_start {
        "true"
    } else {
        "false"
    };
    let _ = writeln!(
        out,
        "{ind}let enable_eager_workflow_start = opts.enable_eager_workflow_start.unwrap_or({eager_default});",
    );
    // Retry policy mirrors the timeout pattern: stays `Option<…>`, falls
    // through to a baked-in proto default when the caller leaves it `None`.
    match wf.retry_policy.as_ref() {
        Some(spec) => {
            let literal = retry_policy_literal(spec);
            let _ = writeln!(
                out,
                "{ind}let retry_policy = opts.retry_policy.or_else(|| Some({literal}));",
            );
        }
        None => {
            let _ = writeln!(out, "{ind}let retry_policy = opts.retry_policy;");
        }
    }
    // R7 — search attributes. Static literal maps fold into a generated
    // `HashMap<String, Payload>` construction that calls the bridge's
    // per-type encoders. Empty / unset stays `None`.
    let sa_literal = search_attributes_literal(wf.search_attributes.as_ref());
    let _ = writeln!(out, "{ind}let search_attributes = {sa_literal};");
}

/// Build the expression that fills the start path's `search_attributes`
/// variable. Returns:
/// * `None` for proto-unset *and* `SearchAttributesSpec::Empty` (the
///   slice-1 no-op).
/// * `Some(HashMap::from([…]))` for a slice-2 `Static` map.
///
/// Keeps the wire-format on the right side of the orphan rule by
/// constructing each entry through one of the bridge's
/// `encode_search_attribute_*` helpers.
fn search_attributes_literal(spec: Option<&crate::model::SearchAttributesSpec>) -> String {
    use crate::model::{SearchAttributeLiteral, SearchAttributesSpec};
    match spec {
        // Slice-1 no-op and "unset" produce the same `None`. The bridge
        // call expects `Option<HashMap<String, Payload>>` so we spell
        // out the type fully — generic inference can't see through the
        // None to the Payload parameter otherwise.
        None | Some(SearchAttributesSpec::Empty) => {
            "::std::option::Option::<::std::collections::HashMap<::std::string::String, temporal_runtime::ProtoPayload>>::None"
                .to_string()
        }
        Some(SearchAttributesSpec::Static(entries)) => {
            let mut s = String::from(
                "Some({ let mut sa: ::std::collections::HashMap<::std::string::String, _> = ::std::collections::HashMap::new(); ",
            );
            for (key, lit) in entries {
                let encoder_call = match lit {
                    SearchAttributeLiteral::String(v) => format!(
                        "temporal_runtime::encode_search_attribute_string(\"{}\")",
                        v.escape_default()
                    ),
                    SearchAttributeLiteral::Int(v) => {
                        format!("temporal_runtime::encode_search_attribute_int({v}i64)")
                    }
                    SearchAttributeLiteral::Bool(v) => {
                        format!("temporal_runtime::encode_search_attribute_bool({v})")
                    }
                    SearchAttributeLiteral::Double(v) => {
                        // `{:?}` keeps the decimal on whole-number f64s
                        // (e.g. `1.0` stays `1.0`); the bridge encoder
                        // returns Result and refuses NaN/infinity, but
                        // parse-time validation already filters those out
                        // so `.expect` here marks a compile-time invariant.
                        format!(
                            "temporal_runtime::encode_search_attribute_double({v:?}f64).expect(\"compile-time-finite f64 literal\")"
                        )
                    }
                    SearchAttributeLiteral::StringField(field) => format!(
                        "temporal_runtime::encode_search_attribute_string(input.{field}.as_str())"
                    ),
                    SearchAttributeLiteral::IntField { rust_field, widen } => {
                        let value_expr = if *widen {
                            format!("input.{rust_field} as i64")
                        } else {
                            format!("input.{rust_field}")
                        };
                        format!(
                            "temporal_runtime::encode_search_attribute_int({value_expr})"
                        )
                    }
                    SearchAttributeLiteral::BoolField(field) => format!(
                        "temporal_runtime::encode_search_attribute_bool(input.{field})"
                    ),
                    SearchAttributeLiteral::DoubleField {
                        rust_field,
                        is_f32,
                    } => {
                        // f32 inputs widen to f64 at the call site so the
                        // bridge encoder's f64 signature works uniformly.
                        // Runtime NaN / infinity surfaces a panic at the
                        // start path (bridge encoder bails on non-finite).
                        let value_expr = if *is_f32 {
                            format!("input.{rust_field} as f64")
                        } else {
                            format!("input.{rust_field}")
                        };
                        format!(
                            "temporal_runtime::encode_search_attribute_double({value_expr}).expect(\"search_attribute double value must be finite at runtime\")"
                        )
                    }
                };
                s.push_str(&format!(
                    "sa.insert(\"{}\".to_string(), {encoder_call}); ",
                    key.escape_default()
                ));
            }
            s.push_str("sa })");
            s
        }
    }
}

/// Emit a `temporal_runtime::RetryPolicy { … }` literal that reproduces the
/// proto-declared default. The bridge struct keeps `backoff_coefficient` behind
/// a private bits-field for `Eq`, so the coefficient lands via the
/// `with_backoff_coefficient` builder setter rather than a struct-init field.
fn retry_policy_literal(spec: &crate::model::RetryPolicySpec) -> String {
    let mut s = String::from("{ let mut rp = temporal_runtime::RetryPolicy::new(); ");
    if let Some(d) = spec.initial_interval {
        s.push_str(&format!(
            "rp.initial_interval = Some(Duration::new({}, {})); ",
            d.as_secs(),
            d.subsec_nanos()
        ));
    }
    if let Some(d) = spec.max_interval {
        s.push_str(&format!(
            "rp.max_interval = Some(Duration::new({}, {})); ",
            d.as_secs(),
            d.subsec_nanos()
        ));
    }
    if spec.max_attempts != 0 {
        s.push_str(&format!("rp.max_attempts = {}; ", spec.max_attempts));
    }
    if !spec.non_retryable_error_types.is_empty() {
        s.push_str("rp.non_retryable_error_types = vec![");
        for (i, e) in spec.non_retryable_error_types.iter().enumerate() {
            if i > 0 {
                s.push_str(", ");
            }
            s.push_str(&format!("\"{}\".to_string()", e.escape_default()));
        }
        s.push_str("]; ");
    }
    let coefficient = spec.backoff_coefficient();
    if coefficient != 0.0 {
        s.push_str(&format!("rp.set_backoff_coefficient({coefficient:?}); "));
    }
    s.push_str("rp }");
    s
}

